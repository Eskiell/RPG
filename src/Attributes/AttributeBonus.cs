    /// <summary>
    ///     Bônus linear para cada ponto de Endurance.
    /// </summary>
    public const float Endurance = 0.10f;

    /// <summary>
    ///     Bônus linear para cada ponto de Strength.
    /// </summary>
    public const float Strength = 0.10f;

    /// <summary>
    ///     Bônus linear para cada ponto de Intelligence.
    /// </summary>
    public const float Intelligence = 0.10f;

    /// <summary>
    ///     Bônus linear para cada ponto de Faith.
    /// </summary>
    public const float Faith = 0.10f;

    /// <summary>
    ///     Bônus percentual para cada ponto de Dexterity (1,5%).
