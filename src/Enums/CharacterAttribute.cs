namespace RPG.Enums;

/// <summary>Os oito atributos primários que definem as capacidades de um personagem.</summary>
public enum CharacterAttribute
{
    /// <summary>Aumenta o HP máximo.</summary>
    Vitality,
    /// <summary>Relacionado à recuperação e resistência geral.</summary>
    Vigor,
    /// <summary>Escala dano de armas pesadas e físicas.</summary>
    Strength,
    /// <summary>Escala dano de armas ágeis e velozes.</summary>
    Dexterity,
    /// <summary>Escala magia arcana e aumenta o MP.</summary>
    Intelligence,
    /// <summary>Escala magia divina e aumenta o MP.</summary>
    Faith,
    /// <summary>Aumenta a stamina máxima.</summary>
    Endurance,
    /// <summary>Escala magias sombrias e efeitos de status.</summary>
    Arcane
}
